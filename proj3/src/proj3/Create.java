    /**
     *Closes open connection. Disposes this window. Sets the parent visible.
     */
    private void quit(){
        // close the connection to database
        try {
             connection.close();
        } catch(SQLException e) {
             e.printStackTrace();
        }  
        dispose();
        myparent.setVisible(true);
    }
    
