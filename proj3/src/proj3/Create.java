    /**
     * Inserts question into database. Resets form.
     */
    private void SaveQuestion(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveQuestion
        saveQuestion.setVisible(false);
        
        try{
            Statement s = connection.createStatement();
            s.executeQuery("insert into questions (question, answer, a, b, c, d) values ('" + question.getText() + "', '" + answer.getText() + "', '" + a.getText() + "', '" + b.getText() + "', '" + c.getText() + "', '" + d.getText() + "');");
        }catch(SQLException e){
            e.printStackTrace();
        }
        
        question.setText("");
        a.setText("");
        b.setText("");
        c.setText("");
        d.setText("");
        answer.setText("");
        question.setText("");
        question.setEditable(false);
        a.setEditable(false);
        b.setEditable(false);
        c.setEditable(false);
        d.setEditable(false);
        answer.setEditable(false);        
    }//GEN-LAST:event_SaveQuestion

    /**
     * Called when a new question is desired. Changes fields to editable.
     */
    private void Show(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Show
        if(!question.getText().equals("")){
            int choice = javax.swing.JOptionPane.showConfirmDialog(this, "Would you like to save this question?");
            if(choice == 0)
                SaveQuestion(evt);
            else if(choice == 2)
                return;
        }
        saveQuestion.setVisible(true);
        question.setText("");
        a.setText("");
        b.setText("");
        c.setText("");
        d.setText("");
        answer.setText("");
        question.setEditable(true);
        a.setEditable(true);
        b.setEditable(true);
        c.setEditable(true);
        d.setEditable(true);
        answer.setEditable(true);
        if(!question_panel.isVisible()){
            question_panel.setVisible(true);
        }        
    }//GEN-LAST:event_Show

