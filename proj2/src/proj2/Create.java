    /**
     *Saves a question to the file.
     */
    private void SaveQuestion(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveQuestion
        saveQuestion.setVisible(false);
        //jPanel1.setVisible(false);
        writer.println(((String)(jComboBox1.getSelectedItem())).toLowerCase());
        writer.println(Integer.parseInt(jTextField1.getText()));
        writer.println("--" + jTextArea1.getText());
        writer.println("--" + jTextArea2.getText());
        writer.flush();
        jTextArea1.setText("");
        jTextArea2.setText("");
        jTextField1.setText("10");
        jTextField1.setColumns(5);
        jLabel7.setText(String.valueOf(++total));
        jTextField1.setEditable(false);
        jTextArea1.setEditable(false);
        jTextArea2.setEditable(false);
        jComboBox1.setEditable(false);
    }//GEN-LAST:event_SaveQuestion

    private void Show(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Show
        if(!jTextArea1.getText().equals("")){
            int choice = javax.swing.JOptionPane.showConfirmDialog(this, "Would you like to save this question?");
            if(choice == 0)
                SaveQuestion(evt);
            else if(choice == 2)
                return;
        }
        saveQuestion.setVisible(true);
        jTextArea1.setText("");
        jTextArea2.setText("");
        jTextField1.setText("10");
        jTextField1.setColumns(5);
        jTextField1.setEditable(true);
        jTextArea1.setEditable(true);
        jTextArea2.setEditable(true);
        jComboBox1.setEditable(true);
        if(!jPanel1.isVisible()){
            jPanel1.setVisible(true);
        }
        
    }//GEN-LAST:event_Show

    
