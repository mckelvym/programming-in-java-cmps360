    /**
     *Opens the file for writing.
     */
    public boolean openFile(){
        boolean success = true;
        writer = null;
        try{
            writer = new PrintWriter(new BufferedWriter(new FileWriter(filename)));            
        }catch(Exception e){
            e.printStackTrace();
            success = false;
        }
        return success;
    }
    
